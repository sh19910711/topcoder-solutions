namespace solution {
    const string YES = "John";
    const string NO = "Brus";
    struct Solution {
        string solve( long long n ) {
            if ( n % 2 )
                return NO;
            int p = 1;
            while ( n >= 2 && n % 2 == 0 ) {
                p ^= 1;
                n /= 2;
            }
            if ( n == 1 && ! p )
                return NO;
            return YES;
        }
    };

}

string TheNumberGameDivOne::find( long long n ) {
    return solution::Solution().solve(n);
}

